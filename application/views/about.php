        <h2><strong>Sistem Drainase</strong></h2>

        <p class="justify">Drainase berasal dari bahasa Inggris yaitu drainage yang mempunyai arti mengalirkan,
            menguras, membuang, mengalihkan air, atau mengeringkan suatu wilayah tertentu dari genangan
            air. Saluran drainase dibangun dengan tujuan untuk melewatkan laju air atau debit rencana
            dengan aman.<br>

            Dalam kaitannya dengan salinitas, drainase diartikan sebagai usaha untuk mengontrol kualitas
            air tanah. Jadi, drainase tidak hanya menyangkut air permukaan tapi juga air tanah.<br>

            Dalam bidang teknik sipil, drainase adalah salah satu upaya teknis dengan membuat saluran
            air atau jalur pembuangan air untuk mengurangi kelebihan air yang berasal dari air hujan,
            rembesan, dan kelebihan air irigasi dari suatu kawasan atau lahan. Jika penanganan drainase
            kurang baik, maka akan mengakibatkan tergenangnya lingkungan sekitar saluran drainase yang
            pada akhirnya menyebabkan pencemaran lingkungan.</p><br>

        <h2><strong>Fungsi Drainase</strong></h2>

        <p class="justify">Adapun fungsi drainase adalah sebagai berikut:</p>
        <p>1. Untuk mengurangi atau membuang kelebihan air dari suatu wilayah, jalan, dan lahan, sehingga
            lahan dapat kembali difungsikan secara optimal.</p>
        <p>2. Membebaskan suatu wilayah, terutama wilayah dengan jumlah kepadatan penduduk yang banyak,
            dari genangan air, erosi dan banjir.</p>
        <p>3. Berfungsi untuk memperkecil risiko kesehatan lingkungan, bebas dari malaria (nyamuk), demam
            berdarah dengue (DBD), dan penyakit lainnya yang dapat menimbulkan kerugian.</p>
        <p>4. Berfungsi untuk menciptkan sistem tata guna lahan yang baik yang dapat dioptimalkan dan juga
            memperkecil kerusakan-kerusakan struktur tanah untuk jalan dan bangunan lainnya.</p><br>

        <h2><strong>Perencanaan Sistem Saluran Drainase</strong></h2>

        <p class="justify">Sistem saluran drainase harus direncanakan agar dapat melewatkan debit rencana dengan baik
            dan aman tanpa melebihi tinggi muka hulu air. Pada umumnya sebuah infrastruktur sistem
            drainase terdiri dari saluran penerima (interceptor drain), saluran pengumpul (collector
            drain), saluran pembawa (conveyor drain), saluran induk (main drain), dan badan air penerima
            (receiving waters).<br>
            Dalam pembuatan perencanaan sistem drainase harus memperhatikan data curah hujan, tata guna
            lahan dan dimensi saluran. Adapun tahapan-tahapan yang digunakan dalam perencanaan teknis
            saluran drainase adalah sebagai berikut:<br>
        <p>1. Menentukan dan menghitung debit rencana.</p>
        <p>2. Membuat jalur saluran sistem drainase.</p>
        <p>3. Merencanakan profil memanjang saluran.</p>
        <p>4. Membuat perencanaan titik-titik yang akan dibangun untuk penampang melintang saluran agar
            badan jalan cepat kering.</p>
        <p>5. Mengatur dan merencanakan infrastruktur serta fasilitas sistem drainase.</p><br>

        <h2><strong>Permasalahan Sistem Drainase dan Cara Menanggulanginya</strong></h2>

        <p>Sistem drainase yang telah dibangun di wilayah perkotaan tidaklah luput dari permasalahan.
            Oleh karenanya dibutuhkan pertimbangan yang matang dalam perencanaannya, yaitu antara lain :</p>

        <h4>1. Peningkatan Debit</h4>

        <p>Menumpuknya sampah dalam saluran drainase akan berdampak terhadap percepatan
            pendangkalan/penyempitan saluran dan sungai. Untuk itu, diperlukan manajemen sampah yang
            baik sehingga saluran drainase tetap dapat menampung debit air dengan aman. Jika kapasitas
            saluran drainase dalam keadaan aman, maka sungai dan saluran drainase tersebut akan mampu
            menampung debit yang terjadi dan tidak akan menimbulkan meluapnya air ke jalanan.</p>

        <h4>2. Penataan Lingkungan</h4>

        <p>Sistem drainase yang sudah ada dapat mengalami masalah jika adanya perumahan-perumahan baru
            terutama yang dibangun oleh developer/pengembang tanpa diikuti dengan penataan drainase yang
            memadai. Bangunan-bangunan tersebut akan mempersempit dimensi saluran drainase. Selain itu,
            perubahan bentuk kontur yang diakibatkan dengan adanya pemukiman baru sebagian telah merubah
            arah aliran sistem drainase sehingga berdampak pada kesenjangan antara rencana penataan
            drainase dengan kenyataan.</p>

        <h4>3. Perubahan Tata Guna Lahan</h4>
        <p>Daerah-daerah seperti bekas persawahan, pada awalnya sistem drainase yang ada merupakan
            saluran irigasi. Jika diubah menjadi sistem drainase, maka dibutuhkan perubahan desain
            saluran.
            Perubahan tata guna lahan yang semula kawasan kosong menjadi wilayah dengan
            bangunan-bangunan barunya akan menyebabkan kawasan tersebut memiliki wilayah resapan yang
            sangat kecil.
            Sebagian saluran yang ada masih saluran alam padahal lahan yang semula kosong telah menjadi
            pemukiman padat.</p>
        <h4>4. Kapasitas Saluran yang Minim</h4>

        <p>Sistem drainase yang sudah ada kurang mampu menampung kapsitas debit air hujan padahal lahan
            untuk pengembangan saluran drainase sudah tidak ada. Ini banyak terjadi di kota besar
            seperti Jakarta, sehingga membutuhkan normalisasi sungai.</p>

        <h4>5. Penyalahgunaan Fungsi</h4>

        <p>Penyalahgunaan fungsi sistem drainase itu sendiri seperti saluran drainase yang juga
            digunakan untuk membuang limbah home industry atau pabrik. Penyalahgunaan ini lebih dikenal
            dengan istilah mixed used, yaitu saluran drainase yang berfungsi campuran untuk drainase dan
            juga saluran limbah.</p>

        <h4>6. Kurangnya Peran Masyarakat</h4>

        <p>Kurangnya kesadaran masyarakat/partisipasi masyarakat yang rendah, tidak membuang sampah
            pada tempatnya, malah membuang sampah ke selokan pada saluran drainase sehingga
            mengakibatkan jalan air tidak lancar.</p>
